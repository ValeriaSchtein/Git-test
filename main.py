# Программа принимает число и показывает его, а также число на 3 больше

# Запрашиваем число у пользователя (input возвращает строку)
число_от_пользователя = input("Введите число: ")

# Преобразуем строку в число (float для дробных, int только для целых)
число = float(число_от_пользователя)

# Вычисляем число на 3 больше
число_плюс_три = число + 3

# Выводим результаты
print(f"Вы ввели: {число}")
print(f"Число на 3 больше: {число_плюс_три}")
# Вычисляем число, умноженное на 2
число_умноженное_на_два = число * 2