"""
–ü—Ä–æ—Å—Ç–æ–π —ç—Ö–æ–±–æ—Ç –¥–ª—è Telegram.
–ü–æ–≤—Ç–æ—Ä—è–µ—Ç (—ç—Ö–æ) –∫–∞–∂–¥–æ–µ —Ç–µ–∫—Å—Ç–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
"""

import asyncio
import logging
from dotenv import load_dotenv
from aiogram import Bot, Dispatcher, F
from aiogram.types import Message
from aiogram.filters import Command
import os

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–∑ —Ñ–∞–π–ª–∞ .env (—Ç–æ–∫–µ–Ω –±–æ—Ç–∞ —Ö—Ä–∞–Ω–∏—Ç—Å—è —Ç–∞–º)
load_dotenv()

# –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ ‚Äî –≤–∏–¥–∏–º –≤ –∫–æ–Ω—Å–æ–ª–∏, —á—Ç–æ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç
logging.basicConfig(level=logging.INFO)

# –ë–µ—Ä—ë–º —Ç–æ–∫–µ–Ω –±–æ—Ç–∞ –∏–∑ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –æ–∫—Ä—É–∂–µ–Ω–∏—è (–±–µ–∑–æ–ø–∞—Å–Ω–µ–µ, —á–µ–º –≤ –∫–æ–¥–µ)
BOT_TOKEN = os.getenv("BOT_TOKEN")

if not BOT_TOKEN:
    raise ValueError("–°–æ–∑–¥–∞–π—Ç–µ —Ñ–∞–π–ª .env –∏ —É–∫–∞–∂–∏—Ç–µ –≤ –Ω—ë–º BOT_TOKEN=–≤–∞—à_—Ç–æ–∫–µ–Ω")

# –°–æ–∑–¥–∞—ë–º –æ–±—ä–µ–∫—Ç—ã –±–æ—Ç–∞ –∏ –¥–∏—Å–ø–µ—Ç—á–µ—Ä–∞
# –î–∏—Å–ø–µ—Ç—á–µ—Ä ‚Äî —ç—Ç–æ ¬´—Ä–∞—Å–ø—Ä–µ–¥–µ–ª–∏—Ç–µ–ª—å¬ª: –æ–Ω —Ä–µ—à–∞–µ—Ç, –∫–∞–∫–æ–π –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –≤—ã–∑–≤–∞—Ç—å –¥–ª—è —Å–æ–æ–±—â–µ–Ω–∏—è
bot = Bot(token=BOT_TOKEN)
dp = Dispatcher()


@dp.message(Command("start"))
async def cmd_start(message: Message):
    """
    –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–æ–º–∞–Ω–¥—ã /start.
    –í—ã–∑—ã–≤–∞–µ—Ç—Å—è, –∫–æ–≥–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–ø–µ—Ä–≤—ã–µ –ø–∏—à–µ—Ç –±–æ—Ç—É –∏–ª–∏ –Ω–∞–∂–∏–º–∞–µ—Ç ¬´–ù–∞—á–∞—Ç—å¬ª.
    """
    await message.answer(
        "–ü—Ä–∏–≤–µ—Ç! üëã –Ø —ç—Ö–æ–±–æ—Ç ‚Äî –ø—Ä–æ—Å—Ç–æ –Ω–∞–ø–∏—à–∏ —á—Ç–æ —É–≥–æ–¥–Ω–æ, –∏ —è –ø–æ–≤—Ç–æ—Ä—é —ç—Ç–æ."
    )


@dp.message(F.text)
async def echo_handler(message: Message):
    """
    –û–±—Ä–∞–±–æ—Ç–∫–∞ –ª—é–±–æ–≥–æ —Ç–µ–∫—Å—Ç–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è.
    F.text ‚Äî —Ñ–∏–ª—å—Ç—Ä: —Å—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Ç–æ–ª—å–∫–æ –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏—è —Å —Ç–µ–∫—Å—Ç–æ–º.
    –ë–æ—Ç –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —Ç–æ—Ç –∂–µ —Ç–µ–∫—Å—Ç –æ–±—Ä–∞—Ç–Ω–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é.
    """
    await message.answer(message.text)


async def main():
    """–ó–∞–ø—É—Å–∫ –±–æ—Ç–∞ ‚Äî –æ–ø—Ä–æ—Å Telegram API –Ω–∞ –Ω–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è."""
    # –£–¥–∞–ª—è–µ–º –≤–µ–±—Ö—É–∫, –µ—Å–ª–∏ –±—ã–ª (–¥–ª—è —á–∏—Å—Ç–æ–≥–æ long polling)
    await bot.delete_webhook(drop_pending_updates=True)
    # –ó–∞–ø—É—Å–∫–∞–µ–º –æ–±—Ä–∞–±–æ—Ç–∫—É –≤—Ö–æ–¥—è—â–∏—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
    await dp.start_polling(bot)


if __name__ == "__main__":
    # asyncio.run() –∑–∞–ø—É—Å–∫–∞–µ—Ç –Ω–∞—à—É –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é main()
    asyncio.run(main())
